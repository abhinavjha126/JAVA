package CONDITIONAL_STATEMENTS;
public class Break_and_Continue                                                                                   
{
	public static void main(String[] args)
	{
for(int i=1;i<=15;i++)
{
	System.out.println(i);
	if(i==8)
	{
		System.out.println("ENDING THE LOOP");                                               
		break;
    }
}                                                                                                                                 
	}
}
